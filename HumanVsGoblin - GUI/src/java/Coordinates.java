public class Coordinates {
    public int x;
    public int y;

    public Coordinates() {
    }

    public int getX(int i) {
        return x;
    }

    public int getY(int j) {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
